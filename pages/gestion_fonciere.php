		<h3>MISSIONS DE LA <i>SAPCO-SENEGAL</i> EN MATIERE <em> D’ACQUISITION FONCIERE</em></h3>

	
</div>
<div class="gs_det">
<p>L’Etat du Sénégal, à travers la Convention générale du 21 Novembre 2013 a confié à la SAPCO-Sénégal une mission de sauvegarde aux fins de valorisation des zones foncières recelant un fort potentiel touristique (Zones d’intérêt touristique).</p>
<br>
<p>Cette mission se décline à travers toute l’étendue du territoire national suivant des zones prioritaires préalablement définies (Delta du Saloum, Zone Nord, Petite-côte, Zone sud et Zone orientale) au niveau desquelles la réalisation de projets non touristiques ne pourra intervenir sauf s’ils ont été agréés par la SAPCO-Sénégal.</p>
<br>
<p> Dans ce cadre la SAPCO-Sénégal identifie, répertorie, cartographie, mène des études, fait immatriculer et acquiert toute assiette foncière présentant des attraits tant naturels que culturels susceptible de faire l’objet d’une valorisation touristique.</p>
<br>
<p>Ce fut le cas sur 600 hectares au niveau de la Petite-côte, ce qui a abouti au fleuron de notre tourisme qu’est la station de Saly Portudal et le processus a été poursuivi avec l’acquisition d’assiettes foncières de Pointe Sarène (110 ha) et Mbodiène (504 ha) en cours d’aménagement pour nos futures Zones touristiques intégrées de deuxième génération.</p>
<br>
<p>La SAPCO-Sénégal détient également dans son patrimoine foncier six titres dans le Delta du Saloum, Eden de biodiversité et de culture classé patrimoine mondial de l’UNESCO.</p>
<br>
<p>Elle a entrepris d’investir les territoires méridionales et septentrionales du pays afin d’en valoriser toutes les richesses touristiques notamment les éléments de patrimoine.</p>
<br>
<p>Ainsi des opérations sont en cours dans la zone Nord mais également au Sud et au Sud-Est afin d’offrir une large et diversifiée gamme de biens permettant de répondre aux besoins d’aménagement et de développement de produits touristiques de rang.
</p>
