  		<h5>
  			ACCOMPAGNEMENT A TOUTES LES ETAPES
  		</h5>
  		<h6>Période préparatoire

</h6>
<br>

<p>La SAPCO-Sénégal consent à tous les promoteurs dignes d’intérêt qui en font la demande dans la limite des disponibilités, une pré-option durant laquelle le promoteur remet à la SAPCO-Sénégal un dossier préalable dans lequel figurent notamment les indications suivantes :</p>
<br>
<p> Désignation des promoteurs, surface financière, répartition du capital prévu ;</p>
<p>Caractéristiques de l’établissement projeté ;</p>
<p>Montant de l’investissement ;</p>
<p>Sources de financement</p>
<br>
<strong>Régime des options conditionnelles – Etudes des dossiers Contrôle a priori</strong>
<br>
<br>
<p>A l’issue de   cette période s’ il apparaît à la SAPCO-Sénégal que le projet  se présente de façon favorable,elle signera avec son partenaire un protocole d’accord cadre. Le dossier complet de l’opération doit être présenté suffisamment à temps  pour permettre d’y apporter éventuellement les modifications ou aménagements jugés nécessaires avant la transformation de l’accord en contrat.</p>
<br>
<p>La SAPCO-Sénégal étudie les dossiers qui sont présentés et s’assure d’une part du caractère régulier des opérations projetées principalement sur le plan financier, d’autre part de la conformité du projet avec les dispositions légales et sanitaires, avec le Schéma – Directeur et plan d’aménagement de la zone avec le règlement d’urbanisme et aussi avec le cahier des charges d’aménagement et d’architecture préparé par ses services.</p>
<br>
<p>Si les conclusions sont satisfaisantes la SAPCO Sénégal donne alors son agrément au projet. Dans le cas contraire, elle oppose un refus lequel doit être motivé.</p>
<br>
<p>Dans tous les cas, la SAPCO Sénégal perçoit à titre définitif, une rémunération pour frais d’étude de dossier.</p>
<br>
<strong>Régime de l’Option Irréversible</strong>
<br>
<br>
<p>En même temps qu’elle donne son agrément au promoteur, la SAPCO-Sénégal transforme alors le protocole d’accord cadre en un contrat de réalisation de projet touristique comportant une cession de droit de propriété. Cette promesse énonce  les clauses principales de cette cession. L’octroi de cette option est subordonné aux conditions suivantes :

</p>
<br>
<p>Accusé de réception par le promoteur de la notification de l’agrément de la SAPCO Sénégal ;
</p>
<p>Dans le cas où un promoteur veut obtenir la levée des réserves formulées par la SAPCO-Sénégal, engagement de modifier son programme conformément aux observations jointes à l’agrément ;</p>
<p>Engagement du promoteur de participer à la gestion de la station dans le cadre du comité de station.
A partir du moment où la SAPCO Sénégal a donné son agrément au programme, elle fournit aux promoteurs toute l’assistance nécessaire, pour faciliter la réalisation de leur projet.</p>
  <br>
  <strong>Réalisation du programme – Contrôle pendant cette période</strong>
  <br>
  <br>
  <p>Lorsque le promoteur, ayant obtenu l’agrément de la SAPCO-Sénégal, le permis de construire, et le financement de son projet, en entame la réalisation, la SAPCO- SENEGAL exerce un contrôle sur les opérations de construction en cours , pour s’assurer que le programme et le planning initialement prévus sont bien respectés

</p>
<br>
<strong>Cession des titres de propriété</strong>
<br>
<p>Lors du démarrage des travaux ,la signature de la promesse de cession, est transformée en cession pure et simple d’un titre de propriété. Les conditions financières de cette cession seront définies par  arrêté conjoint du Ministre chargé des Finances et du Ministre chargé du Tourisme sur proposition de la SAPCO SENEGAL.</p>
  	
  	<br>
  	<strong>Achèvement des travaux – Contrôle a posteriori  ‑ ouverture des établissements</strong>
  	<br>
  	<p>L’ouverture de l’établissement d’hébergement touristique est soumise à l’accord formel de la société en relation avec les services techniques compétents.</p>
 <br> 	
<strong>Contrôle pendant la période de gestion</strong>
<br> 
<br>
<p>La SAPCO-Sénégal exerce, en relation avec les services techniques du Ministère chargé du Tourisme, un triple contrôle pendant toute la période de gestion sur les établissement implantés dans les terrains aménagés.</p>
<br>
<p>Tout d’abord, elle s’assure que les prestations sont de bonne qualité et que la gestion des établissements d’hébergement touristique ne saurait compromettre l’image de marque de la zone touristique.</p>
  	
  <br>
  <p>Les actes transférant aux promoteurs la jouissance des terrains pourront prévoir la possibilité pour la SAPCO-Sénégal, avec l’assistance éventuelle de l’Etat, d’intervenir si la situation des établissements devenait particulièrement critique.</p>
  <br>
  <p>Cette intervention pourra revêtir diverses formes : assistance technique, recherche de nouveaux associés, changement de gérance, voire, dans les cas les plus extrêmes, rachat du titre de propriété.s</p>	
  <br>
  <br>
  <strong>Principes généraux d’attribution des équipements collectifs</strong>
  <br>
  <br>
  <p>Les équipements collectifs édifiés par la SAPCO-Sénégal dans les zones prioritaires conformément à l’étude de factibilité, et aux plans des diverses zones peuvent être loués à des personnes susceptibles de les gérer convenablement et dans l’intérêt de la station.</p>

  <br>
  <p>Les actes de location passés entre la SAPCO-Sénégal et les commerçants devront prévoir dans tous les cas au profit de la SAPCO Sénégal, une clause de résiliation en cas de mauvaise gestion.</p>

<br>
<p>Les commerçants à qui seront donnés en gérance ces équipements collectifs seront sélectionnés par la SAPCO-Sénégal.</p>
<br>
<p>Certains équipements collectifs d’intérêt général ou insuffisamment rentables pourront être gérés directement par la SAPCO Sénégal.</p>
  	
