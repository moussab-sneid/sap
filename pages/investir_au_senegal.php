  		          <div class="dp6">
                  <p>Le Sénégal a fait des progrès significatifs dans l’amélioration de ses infrastructures de transport. Des investissements conséquents ont permis l’expansion des réseaux nationaux et transfrontaliers pour améliorer la mobilité et la sécurité des personnes et des biens, et promouvoir la création de nouveaux centres économiques à travers le pays.<br><br>

                  Grâce au bon état de ses infrastructures (air, terre, mer) et à sa position géographique unique, le Sénégal occupe la première place en Afrique de l’Ouest au classement de la performance logistique, selon la Banque Mondiale. Ayant récemment investi fortement dans ses infrastructures de transport, le Sénégal offre aux entreprises des ouvrages d’art de calibre mondial, basés sur un mécanisme « porte – corridor » qui leur permettent d’expédier plus rapidement leurs marchandises vers les marchés, dans des conditions modernes et performantes.
                  <br>
                  <br>

Les plus grandes compagnies aériennes relient tous les jours Dakar aux centres d’affaires du monde : Paris, Londres, Dubaï, New York, Johannesburg et Air Sénégal S.A., la compagnie aérienne nationale, assure, à partir de Dakar, la correspondance vers l’intérieur du Sénégal et vers d’autres pays.<br><br>

Le Sénégal est une escale maritime performante qui offre un gain de navigation pour les navires venant du Nord par rapport aux autres ports de la Côte Ouest Africaine. Le Port de Dakar offre 24h/24, une gamme de services professionnels et des infrastructures de dernière génération concourant à optimiser les conditions d’escale, de transit et de transbordement.<br>

Selon l’Union Internationale des Télécommunications (UIT), le Sénégal est le premier pays d’Afrique de l’Ouest pour le taux de pénétration et la qualité de services.<br>
