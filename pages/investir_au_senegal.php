Protection des investissements

Le Sénégal est une terre d’opportunités, l’environnement des affaires y est favorable et en constante amélioration. Le gouvernement du Sénégal met en œuvre depuis 2013, le Programme de Réformes de l’Environnement des Affaires et de la Compétitivité (PREAC).

De nombreuses innovations ont été apportées à l’instar de la suppression du capital social pour la création d’une SARL, la création de guichets uniques pour la constitution d’une entreprise ou le raccordement à l’électricité, la dématérialisation des procédures douanières, la réduction des taxes pour les sociétés qui investissent, ainsi que les différentes incitations fiscales permises par le Code des Investissements et le régime d’entreprise franche d’exportation.

La mise sur pied d’un paquet de 52 mesures a propulsé le Sénégal parmi les 10 meilleurs réformateurs au monde par le classement « Doing Business » établi par la Banque Mondiale et ses partenaires.

Le Sénégal a signé plusieurs Accords de Protection et de Promotion des Investissements (APPI) avec plusieurs dizaines de pays partenaires à travers le monde. Dans un contexte de mondialisation, ces accords améliorent la sécurité juridique des investissements. Ils prévoient le rapatriement libre des capitaux d’investissement et des retours sur investissement, garantissent l’expropriation et prévoient une clause de la Nation la Plus Favorisée (NPF) au traitement des investisseurs. Ils prévoient aussi l’indemnisation des pertes en cas de guerre, de conflit armé ou d’émeute. Le Sénégal est pleinement engagé à améliorer son climat des affaires pour promouvoir les investissements et favoriser une croissance économique tirée par le secteur privé
